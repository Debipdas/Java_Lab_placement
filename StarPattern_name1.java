package com.java.lab;

public class StarPattern_name1 {

	public static void main(String[] args) {

        int data = 5;
         
        // Print i number of stars
        for (int i=1; i<=data; i++)
        {
            for (int j=1; j<=i; j++)
            {
                System.out.print("D");
            }
            System.out.println();
        }

	}

}
